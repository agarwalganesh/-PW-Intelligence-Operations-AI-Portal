function renderBusinessFlowMap(flow) {
  const listContainer = document.getElementById("bf-flowStageList");
  if (!listContainer) return;
  listContainer.innerHTML = '';
  
  flow.forEach((step, idx) => {
    const node = document.createElement("div");
    node.className = `flow-step-node ${idx === 0 ? 'active' : ''}`;
    node.setAttribute("data-index", idx);
    node.innerHTML = `
      <div class="flow-step-number">Step 0${idx + 1}</div>
      <div class="flow-step-label">${step.label}</div>
    `;
    
    node.addEventListener("click", () => {
      document.querySelectorAll("#bf-flowStageList .flow-step-node").forEach(n => n.classList.remove("active"));
      node.classList.add("active");
      
      document.getElementById("bf-flowDetailTitle").innerHTML = `
        <svg style="width: 18px; height: 18px; stroke: currentColor; stroke-width: 2; fill: none;"><circle cx="12" cy="12" r="10"></circle><polyline points="12 6 12 12 16 14"></polyline></svg>
        ${step.title}
      `;
      document.getElementById("bf-flowDetailText").textContent = step.detail;
      document.getElementById("bf-flowDetailExtended").innerHTML = `
        <div class="flow-ext-col">
          <h5>Process Bottleneck</h5>
          <p>${step.bottleneck}</p>
        </div>
        <div class="flow-ext-col">
          <h5>Leverage Point</h5>
          <p>${step.leverage}</p>
        </div>
        <div class="flow-ext-col" style="border-left: 2px solid var(--accent-purple); padding-left: 0.5rem;">
          <h5 style="color: var(--accent-purple);">AI Opportunity</h5>
          <p>${step.aiOpportunity}</p>
        </div>
      `;
    });
    
    listContainer.appendChild(node);
    
    if (idx < flow.length - 1) {
      const arrow = document.createElement("div");
      arrow.className = "flow-arrow";
      listContainer.appendChild(arrow);
    }
  });
  
  // Trigger click on first node
  const firstNode = listContainer.querySelector(".flow-step-node");
  if (firstNode) firstNode.click();
}

