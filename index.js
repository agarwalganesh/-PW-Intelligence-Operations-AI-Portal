function runForecastingModel() {
  const data = VERTICALS_DATA[currentVerticalKey];
  if (!data) return;
  
  const baseVal = data.revenueIntel.forecastBase;
  const priceAdj = parseFloat(document.getElementById("price-adjustment-slider").value) / 100;
  const convAdj = parseFloat(document.getElementById("conversion-adjustment-slider").value) / 100;
  
  const baseProjection = baseVal * (1 + priceAdj) * (1 + convAdj);
  const conservativeProjection = baseProjection * 0.9;
  const optimisticProjection = baseProjection * 1.15;
  
  document.getElementById("forecast-conservative").textContent = "₹" + conservativeProjection.toFixed(1) + " Cr";
  document.getElementById("forecast-base").textContent = "₹" + baseProjection.toFixed(1) + " Cr";
  document.getElementById("forecast-optimistic").textContent = "₹" + optimisticProjection.toFixed(1) + " Cr";
}

