function renderListHelper(id, items, color) {
  const container = document.getElementById(id);
  if (!container) return;
  container.innerHTML = '';
  
  if (!items || items.length === 0) {
    container.innerHTML = `<li>No entries reported.</li>`;
    return;
  }
  
  items.forEach(item => {
    const li = document.createElement("li");
    li.innerHTML = item;
    container.appendChild(li);
  });
}
